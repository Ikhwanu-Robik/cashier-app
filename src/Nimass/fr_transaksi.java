    private void btn_add_to_cartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_to_cartActionPerformed
        if (product_name.getText().equals("-") || product_qty.getText().equals("0")) {
            //the use of == comparison is switched to .equals() due to a bug
            JOptionPane.showMessageDialog(null, "Nothing to be added to cart");
        } else if (!product_name.getText().equals("-") && !product_qty.getText().equals("0")) {
            String[] order = new String[]{product_name.getText(), product_price.getText(), product_qty.getText(), product_total.getText()};
            cart.add(order);
            JOptionPane.showMessageDialog(null, "Order added to cart");
        }
    }//GEN-LAST:event_btn_add_to_cartActionPerformed

