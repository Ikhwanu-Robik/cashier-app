    private void btn_qty_incrementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_qty_incrementActionPerformed
        int qty = Integer.parseInt(product_qty.getText());
        qty++;
        product_qty.setText(Integer.toString(qty));

        int total = 0;
        switch (product_name.getText()) {
            case "Kinder Joy":
                total = 12000 * Integer.parseInt(product_qty.getText());
                product_total.setText(Integer.toString(total));
                break;
            case "Sprite":
                total = 5000 * Integer.parseInt(product_qty.getText());
                product_total.setText(Integer.toString(total));
                break;
            default:
                System.out.println("Product Not Specified");
        }
    }//GEN-LAST:event_btn_qty_incrementActionPerformed

    private void btn_qty_decrementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_qty_decrementActionPerformed
        int qty = Integer.parseInt(product_qty.getText());
        if (qty > 0) {
            qty--;
        }
        product_qty.setText(Integer.toString(qty));

        int total = 0;
        switch (product_name.getText()) {
            case "Kinder Joy":
                total = 12000 * Integer.parseInt(product_qty.getText());
                product_total.setText(Integer.toString(total));
                break;
            case "Sprite":
                total = 5000 * Integer.parseInt(product_qty.getText());
                product_total.setText(Integer.toString(total));
                break;
            default:
                System.out.println("Product Not Specified");
        }
    }//GEN-LAST:event_btn_qty_decrementActionPerformed

    private void btn_cartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cartActionPerformed
        cart_dialog = new JDialog(frame, "CART");
        cart_panel = new JPanel();
        cart_title = new JLabel("<html><h1>CART</h1></html>");
        cart_panel.setBackground(new java.awt.Color(255, 255, 255));

        String[][] data = new String[cart.size()][4];
        for (int i = 0; i < cart.size(); i++) {
            for (int j = 0; j < 4; j++) {
                data[i][j] = cart.get(i)[j];
            }
        }
        String[] columnNames = {"name", "price", "qty", "total"};
        DefaultTableModel dm = new DefaultTableModel(data, columnNames);
        cart_table = new JTable(dm);
        cart_table.setBounds(30, 40, 200, 300);
        sp = new JScrollPane(cart_table);

        cart_title.setFont(new java.awt.Font("Tahoma", 1, 18));

        //create button buy
        btn_buy = new javax.swing.JButton();
        btn_buy.setBackground(new java.awt.Color(255, 204, 0));
        btn_buy.setFont(new java.awt.Font("Tahoma", 1, 36)); // NOI18N
        btn_buy.setForeground(new java.awt.Color(255, 255, 255));
        btn_buy.setText("Buy");
        btn_buy.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                btn_buyActionPerformed(evt);
            }
        });

        cart_panel.add(cart_title);
        cart_panel.add(sp);
        cart_panel.add(btn_buy);

        cart_dialog.add(cart_panel);
        cart_dialog.setSize(500, 720);
        cart_dialog.setVisible(true);
    }//GEN-LAST:event_btn_cartActionPerformed

